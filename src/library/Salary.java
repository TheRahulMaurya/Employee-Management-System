package library;

public class Salary
{
    int salary;
    
    public Salary() {
    }
    
    
    
    public Salary(int salary)
    {
        this.salary=salary;
    }
    
    public int getSalary()
    {
        return this.salary;
    }
    
    public void setSalary(int salary)
    {
        this.salary=salary;
    }
}